package com.detroitlabs.community.model;

public class User{

    private int id;
    private String email;
    private String name;
    private String password;

    public int getId(){
        return id;
    }

    public String getEmail(){
        return email;
    }

    public String getName(){
        return name;
    }

    public String getPassword(){
        return password;
    }
}
