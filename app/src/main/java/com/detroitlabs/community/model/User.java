package com.detroitlabs.community.model;

public class User{

    private int id;
    private String username;
    private String name;
    private String password;

    public int getId(){
        return id;
    }

    public String getUsername(){
        return username;
    }

    public String getName(){
        return name;
    }

    public String getPassword(){
        return password;
    }
}
